#import "DrawViewController.h"

@interface DrawViewController ()

@end

@implementation DrawViewController {
    BOOL mouseSwiped;
    CGPoint lastPoint;
    CGFloat red, green, blue, opacity, brush;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    
    [self.navigationController setNavigationBarHidden:YES];
    
    //set the view
    self.view = [[UIView alloc] initWithFrame:[[UIScreen mainScreen] bounds]];

    //get the drawing context
    [self.view setBackgroundColor:[UIColor whiteColor]];
    
    //set the two image views
    self.mainImage = [[UIImageView alloc] initWithFrame:self.view.frame];
    self.tempDrawImage = [[UIImageView alloc] initWithFrame:self.view.frame];
    
    red = 0.0/255.0;
    green = 0.0/255.0;
    blue = 0.0/255.0;
    brush = 10.0;
    opacity = 1.0;
    
    [self.view addSubview:self.mainImage];
    [self.view addSubview:self.tempDrawImage];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

